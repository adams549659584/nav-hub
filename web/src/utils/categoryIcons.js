/** 过滤存在于 lucide-react 的图标名，避免无效项（forwardRef 组件 typeof 为 object） */
export function resolveCategoryIcons(Icons) {
  return CATEGORY_ICONS.filter((name) => {
    const C = Icons[name];
    return C != null && (typeof C === 'function' || typeof C === 'object');
  });
}
